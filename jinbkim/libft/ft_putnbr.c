#include "libft.h"

int		sign_check(int *n)
{
	if (*n >= 0)
		return (1);
	*n *= -1;
	return (-1);
}

void	ft_putnbr(int n)
{
	int		sign;

	sign = sign_check(&n);
	if (sign == -1)
		write(1, "-", 1);
	if (n >= 10)
		ft_putnbr(n / 10);
	write(1, &"0123456789"[n % 10], 1);
}
